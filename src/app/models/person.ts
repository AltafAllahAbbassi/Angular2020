export class Person {

  constructor(
    public id :number,
    public name:string,
    public firstname:string,
    public path:string,
    public age:number,
    public cin:number,
    public job:string
  ) {
    this.id=id
    this.name=name
    this.firstname=firstname
    this.path=path
    this.age=age
    this.cin=cin
    this.job=job
  }
}
